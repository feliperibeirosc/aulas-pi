import java.util.Scanner;

public class NumerosReaisSimples2 {
    public static void main(String[] args) {
        Scanner scan = new Scanner(System.in);
        float a, b, c;

        a = scan.nextFloat();
        b = scan.nextFloat();
        c = scan.nextFloat();

        System.out.printf("%.2f\n", c);
        System.out.printf("%.2f\n", b);
        System.out.printf("%.2f\n", a);
    }
}
